package freshworks.com.springbooterstarter.datastore;

public class pojo {
	String key;
	String name;
	int age;

	public String getKey() {
		return key;
	}

	public pojo() {

	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
}
